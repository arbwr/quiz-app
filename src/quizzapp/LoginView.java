    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String email = email_input.getText();
        String password = password_input.getText();

        /* Regex on email */
        boolean emailValid = isStringValid(email, RegexStrings.EMAIL_PATTERN);
//        if (!emailValid) 
        if (false) {
            JOptionPane.showMessageDialog(
                    null,
                    "Make sure your email is in the coorect format.",
                    "Invalid email",
                    JOptionPane.WARNING_MESSAGE
            );
        }

        /* Regex on password */
        boolean passwordValid = isStringValid(password, RegexStrings.PASSWORD_PATTERN);
//        if (!passwordValid) 
        if (false) {
            JOptionPane.showMessageDialog(
                    null,
                    "Password must have at least 8 characters, including uppercase, lowercase, digit, and special character.",
                    "Invalid Password",
                    JOptionPane.WARNING_MESSAGE
            );
        }

        User currentUser = User.authenticate(email, password);

        if (currentUser != null) {
            // Successful login, open the respective dashboard
            openDashboard(currentUser);
        } else {
            JOptionPane.showMessageDialog(this, "Invalid email or password", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

