    private void openDashboard(User user) {
        if ("Student".equals(user.getRole())) {
            JFrame dashboardFrame = new Dashboard(user);
            this.setVisible(false);
            dashboardFrame.setVisible(true);
        } else if ("Professor".equals(user.getRole())) {
            JFrame dashboardProfessorFrame = new ProfessorDashboard(user);
            this.setVisible(false);
            dashboardProfessorFrame.setVisible(true);
        }
    }

    public static boolean isStringValid(String string, Pattern pattern) {
        Matcher matcher = pattern.matcher(string);
        return matcher.matches();
    }

